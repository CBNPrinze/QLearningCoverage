def draw_state(state):
    obstacle_channel = state[0]
    charging_station_channel = state[1]
    current_location_channel = state[2]

    # Create a combined matrix for visualization
    visualization_matrix = (
        obstacle_channel * 0.5 +
        charging_station_channel * 0.8 +
        current_location_channel * 0.9
    )

    plt.imshow(visualization_matrix, cmap='viridis', interpolation='none')
    plt.title('Environment State')
    plt.colorbar()
    plt.show()

